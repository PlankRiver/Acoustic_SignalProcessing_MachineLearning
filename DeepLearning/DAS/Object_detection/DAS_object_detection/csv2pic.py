# ===================== 单文件处理逻辑【完全复刻预测脚本】 =========================
def single_csv_to_img(csv_path, save_path):
    # 1. 读取csv + 缺失值填充（和你原版一致）
    df = pd.read_csv(csv_path, dtype=np.float64, header=None)
    s_raw = df.dropna(axis=0, how='all').dropna(axis=1, how='all')
    s_raw = s_raw.fillna(method='ffill').fillna(method='bfill')
    s_raw = s_raw.to_numpy()

    # 左右补零（和你原版一致）
    s_raw = np.pad(s_raw, ((0, 0), (2, 2)), mode='constant')
    m_raw, n_col = s_raw.shape

    # 2. 逐列：100Hz低通滤波 → 8倍下采样 → LPF2功率滤波
    sf_down = np.zeros((int(m_raw / 8), n_col), dtype=np.float32)
    sp_power = np.zeros_like(sf_down)
    for col in range(n_col):
        temp_filter = lfilter(LPF1, 1, s_raw[:, col].astype(np.float32))
        sf_down[:, col] = temp_filter[7::8]   # 8倍下采样
        sp_power[:, col] = lfilter(LPF2, 1, np.square(sf_down[:, col]))

    # 3. 绘制全场功率热力图（裸图：无标题、无坐标轴、无色条）
    fig, ax = plt.subplots(figsize=(16, 9), dpi=DPI, frameon=False)
    ax.imshow(sp_power, cmap='jet', vmin=0, vmax=30, aspect='auto')
    ax.set_axis_off()
    fig.subplots_adjust(left=0, right=1, bottom=0, top=1)

    # 保存图片
    plt.savefig(save_path, dpi=DPI, bbox_inches='tight', pad_inches=0)
    plt.close(fig)
    gc.collect()
    print(f"✅ {os.path.basename(csv_path)} 转换完成")

# ===================== 【适配你的目录结构】批量遍历 =========================
def batch_convert(root_input, root_output):
    os.makedirs(root_output, exist_ok=True)

    # 1. 遍历一级分类文件夹
    for cls_name in os.listdir(root_input):
        cls_folder = os.path.join(root_input, cls_name)
        if not os.path.isdir(cls_folder):
            continue

        # 2. 进入每个分类下的 csv 子文件夹
        csv_folder = os.path.join(cls_folder, "csv")
        if not os.path.exists(csv_folder):
            print(f"⚠️ {cls_name} 下未找到csv文件夹，跳过")
            continue

        # 3. 创建对应输出分类文件夹
        out_cls_folder = os.path.join(root_output, cls_name)
        os.makedirs(out_cls_folder, exist_ok=True)

        # 4. 遍历csv文件夹内所有csv文件
        for file in os.listdir(csv_folder):
            if file.lower().endswith(".csv"):
                csv_path = os.path.join(csv_folder, file)
                img_name = os.path.splitext(file)[0] + ".jpg"
                img_save_path = os.path.join(out_cls_folder, img_name)
                single_csv_to_img(csv_path, img_save_path)

    print("\n🎉 全部文件转换完毕！")

